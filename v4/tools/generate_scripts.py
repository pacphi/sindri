# ── default generators (used when a component has no specific entry) ───────────

def default_upgrade(name: str) -> str:
    # ADR-024: install.sh is the upgrade path. SINDRI_COMPONENT_VERSION is already
    # set by the CLI before it calls upgrade.sh (and will be forwarded to install.sh).
    return f"""\
info "Upgrading {name} to $WANT…"
SCRIPT_DIR="$(cd "$(dirname "${{BASH_SOURCE[0]}}")" && pwd)"
exec bash "$SCRIPT_DIR/install.sh"
"""

def default_uninstall(name: str, binary: str | None = None) -> str:
    bin_name = binary or name
    return f"""\
info "Uninstalling {name}…"
rm -f "$HOME/.local/bin/{bin_name}" 2>/dev/null || true
info "{name} binary removed from ~/.local/bin/{bin_name}."
warn "Configuration files and data were NOT removed."
"""

def default_validate(name: str, binary: str | None = None) -> str:
    bin_name = binary or name
    return f"""\
info "Validating {name}…"
require {bin_name}
{bin_name} --version >/dev/null 2>&1 || die "{bin_name} not responding."
info "{name} OK"
"""

# ── generate ──────────────────────────────────────────────────────────────────

SCRIPTS: dict[str, tuple[dict, callable, str]] = {
    # script_filename: (specific_bodies_dict, default_fallback, label)
    "install.sh":   (INSTALLS,   None,            "install"),
    "upgrade.sh":   (UPGRADES,   default_upgrade, "upgrade"),
    "uninstall.sh": (UNINSTALLS, default_uninstall, "uninstall"),
    "validate.sh":  (VALIDATES,  default_validate, "validate"),
}


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate lifecycle scripts for script-backend components")
    parser.add_argument("--dry-run", action="store_true", help="Print what would be written without writing")
    args = parser.parse_args()

    import yaml as _yaml

    total_written = total_skipped = 0

    for d in sorted(COMP_DIR.iterdir()):
        if not d.is_dir():
            continue
        yf = d / "component.yaml"
        if not yf.exists():
            continue
        m = _yaml.safe_load(yf.read_text())
        install = m.get("install") or {}
        if "script" not in install or install["script"] is None:
            continue

        name = d.name
        meta = m.get("metadata", {})
        title = meta.get("description", name)
        version = meta.get("version", "")
        comp_written = 0

        for filename, (specific, fallback, label) in SCRIPTS.items():
            body = specific.get(name)

            if body is None:
                if fallback is None:
                    # install.sh: no body = skip entirely
                    print(f"  [!] {name}/install.sh: no body defined — skipping component")
                    total_skipped += 1
                    break
                # upgrade/uninstall/validate: use the default
                body = fallback(name)

            content = header(title, label, version) + body
            target = d / filename

            if args.dry_run:
                print(f"  [dry] {name}/{filename} ({len(content)} bytes)")
            else:
                target.write_text(content)
                target.chmod(target.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
            comp_written += 1

        total_written += comp_written

    action = "Would write" if args.dry_run else "Wrote"
    scripts_per = len(SCRIPTS)
    comps = total_written // scripts_per
    print(f"\n{action} {total_written} scripts ({comps} components × {scripts_per} lifecycle scripts).")
    if total_skipped:
        print(f"Skipped {total_skipped} components (no install body defined).")


if __name__ == "__main__":
    main()

